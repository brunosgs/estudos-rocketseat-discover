/*
    VARIÁVEIS
    - Nomes simbolicos para receber algum valor 
    - Atalhos de código
    - Identificadores
    - 3 palavras reservadas para criar uma variável
        * var
        * let
        * const
*/

var clima = "clima = Quente"
clima = "clima = Frio"

let clima2 = "clima2 = Quente"
clima2 = "clima2 = Frio"


// A constante não é possível alter o valor dela.
const clima3 = "clima3 = Quente"
clima3 = "clima3 = Frio"

console.log(clima)
console.log(clima2)
console.log(clima3)