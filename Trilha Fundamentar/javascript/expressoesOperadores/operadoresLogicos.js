/*
    Expressões e operadores

    - Operadores lógicos (logical operators)
        * São dois valores do tipo booleano que quando verificados vai resultar em verdadeiro ou falso.
*/

let pao = true
let queijo = true

// AND (&&)
console.log(pao && queijo)

// OR (||)
console.log(pao || queijo)

// NOT (!)
console.log(!pao)
console.log(!queijo)