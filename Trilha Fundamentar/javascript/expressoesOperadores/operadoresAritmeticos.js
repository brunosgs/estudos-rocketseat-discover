/*
    Expressões e operadores

    - Operadore aritméticos
*/

// Multiplicação *
console.log(3.5 * 5)

// Divisão /
console.log(15 / 5)

// Soma +
console.log(36 + 69)

// Subtração -
console.log(36 - 69)

// Resto da divisão %
let remainder = 11 % 3
console.log(remainder)

// Incremento ++
let increment = 0
increment++ // OBS: ele será incrementando após, só quando ele finalizar essa linha
++increment // OBS: ele será incrementando antes de finalizar a linha
console.log(increment)

// Decremento --
let decrement = 0
decrement-- // Segue a mesma regra do incremento, só vai decrementar após, quando finalizar a linha
console.log(decrement)

// Exponencial **
console.log(3 ** 3)