/*
    Expressões e operadores

    - Operadores de atribuição (Assignment)
*/

// assignment normal:
let assigNormal = 1
console.log(assigNormal)

// addition assignment (adição):
let assigAdicao = 1
assigAdicao += 2
console.log(assigAdicao)

// subtraction assignment (subtração):
let assigSubtracao = 1
assigSubtracao -= 1
console.log(assigSubtracao)

// multiplication assignment (multiplacação):
let assigMultiplicacao = 1
assigMultiplicacao *= 2
console.log(assigMultiplicacao)

// division assignment (divisão):
let assigDivisao = 1
assigDivisao /= 2
console.log(assigDivisao)

// exponetiation assignment (exponenciação):
let assigExponenciacao = 3
assigExponenciacao **= 2
console.log(assigExponenciacao)

// remainder assignment (resto de divisão):
let assigRestoDivisao = 1
assigRestoDivisao %= 2
console.log(assigRestoDivisao)