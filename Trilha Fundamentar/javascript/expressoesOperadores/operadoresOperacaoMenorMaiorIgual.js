/*
    Expressões e operadores

    - Operadores de comparação maior e menor (igual)
*/

let one = 1
let two = 2

// maior que    >
console.log(one > two)

// menor que    <
console.log(one < two)

// maior igual a    >=
console.log(one >= 1)
console.log(two >= 1)

// menor igual a    <=
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)